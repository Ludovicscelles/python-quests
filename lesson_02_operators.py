
### arithmetic operators ###

# addition
addition = 5 + 2
print("Résultat de l'addition :", addition)

# subtraction
subtraction = 5 - 2
print("Résultat de la soustraction :", subtraction)

# multiplication
multiplication = 5 * 2
print("Résultat de la multiplication :", multiplication)

# division
division = 5 / 2
print("Résultat de la division :", division)

# integer division
integer_division = - 22 // 5
print("Résultat de la division entière :", integer_division)

# modulo
modulo = 5 % 2 
print("Résultat du modulo de 5 par 2 :", modulo)

# exponentiation
exponentiation = 5 ** 2
print("Résultat de 5 puissance 2 :", exponentiation)

### arithmetic comparators ###

# lower
comparator01 = 9 < 4.5
print("Résultat de la première comparaison :", comparator01)

# upper or equal
comparator02 = 9 <= 4.5
print("Résultat de la deuxième comparaison :", comparator02)

# upper
comparator03 = 9 > 4.5
print("Résultat de la troisième comparaison :", comparator03)

# upper or equal
comparator04 = 9 >= 4.5
print("Résultat de la quatrième comparaison :", comparator04)

# equal
comparator05 = 9 == 4.5
print("Résultat de la cinquième comparaison :", comparator05)

# different
comparator06 = 9 != 4.5
print("Résultat de la sixième comparaison :", comparator06)

# identity
comparator07 = 9 is 4.5
print("Résultat de la septième comparaison :", comparator07)

# non indentity
comparator08 = 9 is not 4.5
print("Résultat de la huitième comparaison :", comparator08)


### logic operators ###

# and
x = 12 
y = -32
both_positif = x > 0 and y > 0
print("Est-ce que x et y sont tous deux positifs ?", both_positif)

# or
a = -102
b = 5
on_positif = a > 0 or b > 0
print("Est que a ou b est positif ?", on_positif)

# not
c = 58
d = 25
negated_result = not(d >= c)
print("Inversion du résultat final :", negated_result)

